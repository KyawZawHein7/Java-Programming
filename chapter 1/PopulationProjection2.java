public class PopulationProjection2 {
    public static void main(String[] args) {
        System.out.println("-One birth in every 7 seconds");
        System.out.print("-Total birth in next five years =");
        System.out.println((86400 / 7) * 1825);
        System.out.println("");
        System.out.println("-One death in every 13 seconds");
        System.out.print("-Total death in next five years =");
        System.out.println((86400 / 13) * 1825);
        System.out.println("");
        System.out.println("-One new immigrant in every 45 seconds");
        System.out.print("-Total immigrants in next five years =");
        System.out.println((86400 / 45) * 1825);
        System.out.println("");
    }

}
